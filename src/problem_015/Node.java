package problem_015;

public class Node {
  private Node left;
  private Node right;
  private Point point;

  public Node getLeft() {
	return left;
  }

  public void setLeft(Node left) {
	this.left = left;
  }

  public Node getRight() {
	return right;
  }

  public void setRight(Node right) {
	this.right = right;
  }

  public Point getPoint() {
	return point;
  }

  public void setPoint(Point point) {
	this.point = point;
  }
}
